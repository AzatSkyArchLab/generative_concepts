/**
 * Get sun vectors for a latitude.
 * @param {number} latitude - degrees N
 * @returns {{ vectors: Array<[number,number,number]>, timeStep: number, description: string }}
 */
export function getSunVectors(latitude) {
  var lat = parseFloat(latitude);

  // Exact match
  if (LATITUDES[lat]) {
    return {
      vectors: LATITUDES[lat].vectors,
      timeStep: METADATA.timeStepMinutes,
      description: LATITUDES[lat].description
    };
  }

  // Find nearest
  var keys = [];
  for (var k in LATITUDES) {
    if (LATITUDES.hasOwnProperty(k)) keys.push(parseFloat(k));
  }

  var closest = keys[0];
  var minDist = Math.abs(lat - closest);
  for (var i = 1; i < keys.length; i++) {
    var d = Math.abs(lat - keys[i]);
    if (d < minDist) { closest = keys[i]; minDist = d; }
  }

  return {
    vectors: LATITUDES[closest].vectors,
    timeStep: METADATA.timeStepMinutes,
    description: LATITUDES[closest].description + ' (nearest to ' + lat + '°)'
  };
}

/**
 * Get available latitudes.
 * @returns {Array<number>}
 */
export function getAvailableLatitudes() {
  var result = [];
  for (var k in LATITUDES) {
    if (LATITUDES.hasOwnProperty(k)) result.push(parseFloat(k));
  }
  return result.sort(function (a, b) { return a - b; });
}

